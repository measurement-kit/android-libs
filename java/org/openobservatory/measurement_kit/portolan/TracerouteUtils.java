package java.org.openobservatory.measurement_kit.portolan;

import java.util.regex.Pattern;

public class TracerouteUtils {
    public static boolean isIPv6(String address) {
        boolean ret = Pattern.compile("^(?:[0-9a-fA-F]{1,4}:){7}[0-9a-fA-F]{1,4}$").matcher(address).matches();
        if (ret) {
            return ret;
        }
        return Pattern.compile("^((?:[0-9A-Fa-f]{1,4}(?::[0-9A-Fa-f]{1,4})*)?)::((?:[0-9A-Fa-f]{1,4}(?::[0-9A-Fa-f]{1,4})*)?)$").matcher(address).matches();
    }

    public static boolean isIPv4(String address) {
        return Pattern.compile("^(25[0-5]|2[0-4]\\d|[0-1]?\\d?\\d)(\\.(25[0-5]|2[0-4]\\d|[0-1]?\\d?\\d)){3}$").matcher(address).matches();
    }
}
